// Definition for singly-linked list.
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

// @lc code=start
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        if (head == nullptr)
            return nullptr;

        ListNode dummy(0, head);
        ListNode *p1 = head;
        ListNode *p2 = &dummy;

        for (int i = 0; i < n; i++) {
            p1 = p1->next;
        }

        while (p1 != nullptr) {
            p1 = p1->next;
            p2 = p2->next;
        }

        p1 = p2->next;
        p2->next = p1->next;

        delete p1;

        return dummy.next;
    };
};
